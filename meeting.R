# Цикличный подход к подсчету корней значений списка в 22 раза медленнее
v <- 1:1e7
system.time({
  x <- 0
  for (i in v) x[i] <- sqrt(v[i])
})

#Векторный подход к подсчету корней каждого значения из списка в 22 раза быстрее
v <- 1:1e7
system.time({
  x <- sqrt(v)
})

