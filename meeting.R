# Условные выражения обыкновенные
if (1 > 0) {
  print('Mickey win!')
} else
  print('Mickey will eat them')

# Условное выражение сокращенное
ifelse(runif(8) > 0.5, 'Орел', 'Решка')

# Вложенное условное выражение
x <- runif(8)
ifelse(x > 2/3, 'Ножницы',
       ifelse(x > 1/3, 'Камень', 'Бумага')
       )

# Множественный выбор
switch('factorial',
       sum = 5 + 5,
       product = 5 * 5,
       factorial = factorial(5),
       0)


# Цикл repeat (while)
i <- 0
repeat {
  i <- i + runif(1)
  if (i > 5) break
  print(i)
}

# Цикл for
for (i in 1:8) {
  ifelse(i %% 2 == 0,
         print('Четное'),
         print('Нечетное')
         )
}

for (i in letters) {
  if (i == 'b') next
  if (i == 'd') break
  print(i)
}


